        $users->email=$request->email;
        $users->password=md5($request->name);
        $users->is_admin=$request->is_admin;
        $users->save();
        if($users){
            return redirect()->back()->with('success','Kullanıcı Olusturma Basarılı ');
        }
        return redirect()->back()->with('error','İşlem Başarısız');

